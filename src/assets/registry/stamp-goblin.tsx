/* Each row MUST be exactly 48 chars. '.' = transparent. */
const W_PX = 48;

function assertGrid(rows: readonly string[], label: string): readonly string[] {
  for (let i = 0; i < rows.length; i++) {
    const r = rows[i] ?? '';
    if (r.length !== W_PX) {
      throw new Error(
        `stamp-goblin ${label} row ${i} has length ${r.length}, expected ${W_PX}`,
      );
    }
  }
  return rows;
}

